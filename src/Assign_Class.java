    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String t_id=jTextField1.getText();
        String course=(String) jComboBox1.getSelectedItem();
        String sem=(String) jComboBox2.getSelectedItem();
        String subject=(String) jComboBox3.getSelectedItem();
        
        try
        {
            Connection con=c.connect();
            Statement st=con.createStatement();
            String s=String.format("update teacher_class_assigned  set current_class_assigned='%s' where id='%s' ",course,t_id);
            st.executeUpdate(s);
            String s1=String.format("update teacher_class_assigned set semester='%s' where id='%s' ",sem,t_id);
            st.executeUpdate(s1);
            String s2=String.format("update teacher_class_assigned set subjects='%s' where id='%s' ",subject,t_id);
            st.executeUpdate(s2);
            
            jTable1.removeAll();
            String s3=String.format("select * from teacher_class_assigned");
            ResultSet rs=st.executeQuery(s3);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            JOptionPane.showMessageDialog(rootPane,"Class Assigned to Teacher-Id: "+t_id+"\n"+"Course: "+course+"\nSemester: "+sem+"\nSubject: "+subject);
            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


