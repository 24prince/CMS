    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String id = jTextField1.getText();
        
        try
        {
            Connection con=c.connect();
            String s=String.format("select * from student_details where id='%s'",id);
            ResultSet rs=con.createStatement().executeQuery(s);
            while(rs.next())
            {
                this.id=rs.getString("id");
                this.name=rs.getString("full_name");
                this.fname=rs.getString("father_name");
                this.mname=rs.getString("mother_name");
                this.dob=rs.getString("dob");
                this.course=rs.getString("course");
                this.semester=rs.getString("semester");
                this.email=rs.getString("email");
                this.mobile=rs.getString("mobile");
                
            }
            
            String s1=String.format("select * from result_upload where id='%s'",id);
            ResultSet rs1=con.createStatement().executeQuery(s1);
            while(rs1.next())
            {
                this.marks1=rs1.getString("sub1");
                this.marks2=rs1.getString("sub2");
                this.marks3=rs1.getString("sub3");
                this.marks4=rs1.getString("sub4");
                this.marks5=rs1.getString("sub5");
                this.marks6=rs1.getString("sub6");
            }
            
            
            JFileChooser j=new JFileChooser();
            j.showSaveDialog(rootPane);
            File f=j.getSelectedFile();
            String path=f.getAbsolutePath();
            
            Document d=new Document();
            PdfWriter.getInstance(d, new FileOutputStream(path));
            d.open();
            d.add(new Paragraph("                                               INDIAN INSTITUTE OF TECHNOLOGY" +
                    "\n                                                     HAUZ KHAS, NEW DELHI-110016"+
                    "\nDATE :- "+new SimpleDateFormat("dd/MM/yyyy").format(new Date()) +"\n"                  
                    +"_____________________________________________________________________________\n"
                    +"                                                            Personal Details\n"
                    +"_____________________________________________________________________________\n"
                    +"\n"
                    +"Student ID: - "+this.id+"                                        "
                    +"Student Name: - "+this.name+"\n"
                    +"Student Father's Name: - "+this.fname+"             "
                    +"Student Mother's Name: - "+this.mname+"\n"
                    +"Student DOB: - "+this.dob+"                                  "
                    +"Student Course: - "+this.course+"\n" 
                    +"Student Semester: - "+this.semester+"                                          "
                    +"Student Email ID: - "+this.email+"\n"
                    +"Student Mobile: - "+this.mobile+"\n\n"                    
                    +"_____________________________________________________________________________\n"
                    +"                                                              Result\n"
                    +"_____________________________________________________________________________\n\n"                    
                    +""+sub1+":- "+marks1+"     "
                    +""+sub2+":- "+marks2+"     "
                    +""+sub3+":- "+marks3+"     \n"
                    +""+sub4+":- "+marks4+"     "
                    +""+sub5+":- "+marks5+"\n"                    
                    +"\n\n"
                    +"Note:- \n"
                    +"  This is the system generated result and does not need any signature or stamp...."
            
            ));
            d.close();
            int i=JOptionPane.showConfirmDialog(rootPane,"Do you want to open this pdf?");
            if(i==0)
            {
                try
                {
                    Runtime.getRuntime().exec("rundll32 url.dll,FileProtocolHandler "+f.getAbsolutePath());
                    this.dispose();
                }
                catch(Exception e)
                {
                    System.out.println(e);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane,"Your file has been downloaded.");
                this.dispose();
            }
        }
        catch(Exception  e)
        {
            e.printStackTrace();
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

